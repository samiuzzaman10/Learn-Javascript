console.log("Arithematic Operators");
let a = 20, b = 10;

console.log(a + " + " + b + " = " + (a+b));
console.log(a + " - " + b + " = " + (a-b));
console.log(a + " * " + b + " = " + (a*b));
console.log(a + " / " + b + " = " + (a/b));
console.log(a + " % " + b + " = " + (a%b));

