//Relational Operator :
let m = 10,
  n = 15,
  o = 10;
console.log(m + " > " + n + " = " + (m > n));
console.log(m + " < " + n + " = " + (m < n));
console.log(m + " >= " + o + " = " + (m >= o));
console.log(m + " <= " + n + " = " + (m <= n));
console.log(m + " == " + n + " = " + (m == n));
console.log(m + " == " + o + " = " + (m == o));
console.log(m + " != " + n + " = " + (m != n));
console.log(m + " != " + o + " = " + (m != o));
